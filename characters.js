// 8x8 像素帧，1=有像素
function pad8x8(rows) {
  const out = [];
  for (let r = 0; r < 8; r++) {
    const row = rows[r] || [];
    out.push([0,0,0,0,0,0,0,0].map((_, i) => row[i] || 0));
  }
  return out;
}

